    public void fetchitemDetailsCS() {
        Connection c = null;
        Statement stmt = null;
        DefaultTableModel dtm = (DefaultTableModel) Search_Table.getModel();
        dtm.setRowCount(0);
        try {
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:database.db");
            stmt = c.createStatement();
            ResultSet rs1 = stmt.executeQuery("SELECT * FROM employee_data");
            while (rs1.next()) {
                String s1 = rs1.getString("first_name");
                String s2 = rs1.getString("last_name");
                String s3 = rs1.getString("position");
                String s4 = rs1.getString("phone_number");
                String s5 = rs1.getString("id_employee");
                String totals1ands2 = s1+" "+s2;
                String table_data[] = {totals1ands2,s3,s4,s5};
                DefaultTableModel tblModel = (DefaultTableModel) Search_Table.getModel();
                tblModel.addRow(table_data);
            }
        } catch (Exception b) {
            JOptionPane.showMessageDialog(null, b);
        }
    }
    
