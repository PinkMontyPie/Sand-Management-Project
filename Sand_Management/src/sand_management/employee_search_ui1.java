    private void Search_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Search_TableMouseClicked
        // TODO add your handling code here:
        Connection conn = null;
        Statement stmt = null;
        int row = Search_Table.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) Search_Table.getModel();
        Name_Text.setText(model.getValueAt(row, 0).toString());
        position_Text.setText(model.getValueAt(row, 1).toString());
        Phone_Number.setText(model.getValueAt(row, 2).toString());
        ID_Employee.setText(model.getValueAt(row, 3).toString());
        String search = ID_Employee.getText();
        try {
            Class.forName("org.sqlite.JDBC");
            conn = DriverManager.getConnection("jdbc:sqlite:database.db");
            conn.setAutoCommit(false);
            stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery( "SELECT * FROM employee_data WHERE id_employee = '"+search+"'" );
            String address = rs.getString("current_address");
            String id_card1 = rs.getString("id_card");
            Employee_Address.setText(address);
            ID_Card.setText(id_card1);
            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception b) {
            JOptionPane.showMessageDialog(null, b);
        }
    }//GEN-LAST:event_Search_TableMouseClicked

