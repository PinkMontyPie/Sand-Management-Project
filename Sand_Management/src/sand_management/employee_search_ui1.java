    private void DelButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DelButton1MouseClicked
        // TODO add your handling code here:
        Connection c = null;
        PreparedStatement pat = null;
        int row = Search_Table.getSelectedRow();
        String del = Search_Table.getModel().getValueAt(row,3).toString();
        DefaultTableModel model = (DefaultTableModel) Search_Table.getModel();
        try {
            Class.forName("org.sqlite.JDBC");
            c = DriverManager.getConnection("jdbc:sqlite:database.db");
            String delete = "DELETE FROM employee_data WHERE ID_EMPLOYEE = '"+del+"'";
            pat = c.prepareStatement(delete);
            pat.execute();
            int a = JOptionPane.showConfirmDialog(null, "Are you sure to delete this item? : " + model.getValueAt(row, 3).toString(), "Alert", JOptionPane.INFORMATION_MESSAGE);
            if (a == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(null, "This item has been deleted");
                fetchitemDetailsCS();
            }
        } catch (Exception b) {
            JOptionPane.showMessageDialog(null, b);
        }
    }//GEN-LAST:event_DelButton1MouseClicked

